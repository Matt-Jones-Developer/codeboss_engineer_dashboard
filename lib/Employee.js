// define and export the Employee class

class Employee {
  constructor(name, id, email) {
    // define employee
    this.name = name;
    this.id = id;
    this.email = email;
  }

  getName() {
    return this.name;
  }

  getId() {
    return this.id;
  }

  getEmail() {
    return this.email;
  }

  getRole() {
    return 'Employee';
  }

}


module.exports = Employee;
